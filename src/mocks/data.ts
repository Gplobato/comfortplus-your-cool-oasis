export function generateMetricSeries(days = 14): MetricPoint[] {
  const out: MetricPoint[] = [];
  const now = new Date("2026-07-14");
  for (let i = days - 1; i >= 0; i--) {
    const d = new Date(now);
    d.setDate(d.getDate() - i);
    const base = 300 + Math.sin(i / 2) * 60 + Math.random() * 40;
    const spend = 400 + Math.cos(i / 3) * 80 + Math.random() * 60;
    out.push({
      date: d.toISOString().slice(0, 10),
      leads: Math.round(base),
      cpl: Number(((spend / base) * 1.1).toFixed(2)),
      spend: Math.round(spend * 3.6),
      impressions: Math.round((base * 380) + Math.random() * 4000),
    });
  }
  return out;
}
